#uso de elementos de división

n1=int(input('Ingrese el dividendo: '))
n2=int(input('Ingrese el divisor: '))
cociente=n1//n2
print('El cociente de la división es:',cociente)
residuo=n1%n2
print('\nEl residuo de la división es :',residuo)
división=n1/n2
print('\nEl resultado de la división es :',división)
